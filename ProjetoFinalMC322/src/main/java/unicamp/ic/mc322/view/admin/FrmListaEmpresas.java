        private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnSalvarActionPerformed
                boolean naoExiste = true;
                if (!txtCNPJ.getText().equals("  .   .   /    -  ") && !txtCPF.getText().equals("   .   .   -  ")
                                && !txtTelefone.getText().equals("(  )       -    ")
                                && !txtNome.getText().equals("") && !txtNomeDono.getText().equals("")
                                && !txtSenha.getText().equals("")) {
                        for (Empresa e : Main.listaEmpresas) {
                                System.out.println(e.getCnpj() + " " + txtCNPJ.getText());
                                if (e.getCnpj().equals(txtCNPJ.getText())) {
                                        e.setNome(txtNome.getText());
                                        e.setCnpj(txtCNPJ.getText());
                                        e.getDono().setNome(txtNomeDono.getText());
                                        e.getDono().setTelefone(txtTelefone.getText());
                                        e.getDono().setCpf(txtCPF.getText());
                                        e.getDono().setSenha(txtSenha.getText());
                                        naoExiste = false;
                                        JOptionPane.showMessageDialog(null, "Empresa atualizada com sucesso!");
                                }
                        }
                        if (naoExiste) {
                                Funcionario dono = new Gerente(txtCPF.getText(), txtNomeDono.getText(), null,
                                                txtTelefone.getText(), "Dono",
                                                txtSenha.getText(), null);
                                Empresa objEmpresa = new Empresa(txtCNPJ.getText(), txtNome.getText(), dono);
                                Main.usuarios.add(dono);
                                Main.listaEmpresas.add(objEmpresa);
                                JOptionPane.showMessageDialog(null, "Empresa cadastrada com sucesso!");
                                empresaSelecionada = objEmpresa;
                        }
                        atualizarTabelaEmpresa();
                        atualizarTabelaFilial();
                        txtCNPJ.setText("");
                        txtCPF.setText("");
                        txtNome.setText("");
                        txtNomeDono.setText("");
                        txtSenha.setText("");
                        txtSenha.setText("");
                        txtTelefone.setText("");
                }
        }// GEN-LAST:event_btnSalvarActionPerformed

        private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnExcluirActionPerformed
                if (!txtCNPJ.getText().equals("")) {
                        for (Empresa f : Main.listaEmpresas) {
                                if (f.getCnpj().equals(txtCNPJ.getText())) {
                                        Main.listaEmpresas.remove(f);
                                        JOptionPane.showMessageDialog(null, "Empresa excluida com sucesso!");
                                        break;
                                }
                        }

                        atualizarTabelaEmpresa();
                        atualizarTabelaFilial();

                        txtCNPJ.setText("");
                        txtCPF.setText("");
                        txtNome.setText("");
                        txtNomeDono.setText("");
                        txtSenha.setText("");
                        txtSenha.setText("");
                        txtTelefone.setText("");
                }
        }// GEN-LAST:event_btnExcluirActionPerformed

