        private void tblEmpresasMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_tblEmpresasMouseClicked
                int linhaSelecionada = tblEmpresas.getSelectedRow();

                // Aqui você pode realizar as ações desejadas com a linha selecionada
                // Por exemplo, exibir os valores das células selecionadas
                Object valorCelula1 = tblEmpresas.getValueAt(linhaSelecionada, 0);
                Object valorCelula2 = tblEmpresas.getValueAt(linhaSelecionada, 1);

                txtCNPJ.setText(valorCelula1.toString());
                txtNome.setText(valorCelula2.toString());

                for (Empresa e : Main.listaEmpresas) {
                        if (e.getCnpj().equals(txtCNPJ.getText())) {
                                txtNomeDono.setText(e.getDono().getNome());
                                txtCPF.setText(e.getDono().getCpf());
                                txtTelefone.setText(e.getDono().getTelefone());
                                txtSenha.setText(e.getDono().getSenha());
                                empresaSelecionada = e;
                        }
                }
                atualizarTabelaFilial();

        }// GEN-LAST:event_tblEmpresasMouseClicked

        private void tblFiliaisMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_tblFiliaisMouseClicked
                Integer selecao = JOptionPane.showConfirmDialog(null, "Deseja acessar essa filial?");

                if (selecao == 0) {
                        int linhaSelecionada = tblFiliais.getSelectedRow();

                        Object valorCelula1 = tblFiliais.getValueAt(linhaSelecionada, 0);
                        this.admOuDono.setFilial(empresaSelecionada.selecionarFiliais(valorCelula1.toString()));
                        FrmMenu menu = new FrmMenu(admOuDono);
                        menu.setVisible(true);
                        this.dispose();
                }
        }// GEN-LAST:event_tblFiliaisMouseClicked

        private void btnNovaFilialActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnNovaFilialActionPerformed
                if (!txtCNPJ.getText().equals("") && !txtCNPJ.getText().equals("  .   .   /    -  ")) {

                        String nomeFilial = JOptionPane.showInputDialog(null,
                                        "Digite o nome da filia: ",
                                        JOptionPane.PLAIN_MESSAGE);
                        String enderecoFilial = JOptionPane.showInputDialog(null,
                                        "Digite o endereco da filial:",
                                        JOptionPane.PLAIN_MESSAGE);

                        if (nomeFilial != null && enderecoFilial != null) {
                                Filial objFilia = new Filial(nomeFilial, enderecoFilial);
                                for (Empresa e : Main.listaEmpresas) {
                                        if (e.getCnpj().equals(txtCNPJ.getText())) {
                                                e.getListaFiliais().add(objFilia);
                                                JOptionPane.showMessageDialog(null, "Filial cadastrada com sucesso!");
                                                break;
                                        }
                                }
                        }

                        atualizarTabelaFilial();
                } else {
                        JOptionPane.showMessageDialog(null, "Selecione uma empresa");
                }
        }// GEN-LAST:event_btnNovaFilialActionPerformed

