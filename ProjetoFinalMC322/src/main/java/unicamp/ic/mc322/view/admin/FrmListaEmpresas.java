        private void atualizarTabelaFilial() {
                DefaultTableModel model = (DefaultTableModel) tblFiliais.getModel();
                model.setRowCount(0);
                if (empresaSelecionada != null)
                        for (Filial f : empresaSelecionada.getListaFiliais()) {
                                Object[] novaLinha = { f.getId(), f.getNome(), f.getEndereco() }; // Substitua
                                // pelos
                                // dados
                                // que deseja adicionar
                                model.addRow(novaLinha);
                        }
        }

        private void atualizarTabelaEmpresa() {
                DefaultTableModel model = (DefaultTableModel) tblEmpresas.getModel();
                model.setRowCount(0);
                if (Main.listaEmpresas != null)
                        for (Empresa e : Main.listaEmpresas) {
                                Object[] novaLinha = { e.getCnpj(), e.getNome(), e.getDono().getCpf(),
                                                e.getDono().getSenha() }; // Substitua
                                // pelos
                                // dados
                                // que deseja adicionar
                                model.addRow(novaLinha);
                        }
        }

        /**
         * Creates new form FrmListaEmpresas
         */
        public FrmListaEmpresas(Funcionario admOuDono) {
                initComponents();

                this.admOuDono = admOuDono;
                if (admOuDono.getCargo().equals("Dono")) {

                        for (Empresa e : Main.listaEmpresas) {
                                if (e.getDono().getCpf().equals(admOuDono.getCpf())) {
                                        empresaSelecionada = e;
                                        atualizarTabelaFilial();
                                }
                        }

                        this.btnSalvar.setVisible(false);
                        this.btnExcluir.setVisible(false);
                        lblOu.setVisible(false);
                        lblLinkAuth.setVisible(false);

                        txtCNPJ.setText(empresaSelecionada.getCnpj());
                        txtNome.setText(empresaSelecionada.getNome());
                        txtCPF.setText(empresaSelecionada.getDono().getCpf());
                        txtNomeDono.setText(empresaSelecionada.getDono().getNome());
                        txtTelefone.setText(empresaSelecionada.getDono().getTelefone());
                        txtSenha.setText(empresaSelecionada.getDono().getSenha());

                        txtCNPJ.setEditable(false);
                        txtNome.setEditable(false);
                        txtCPF.setEditable(false);
                        txtNomeDono.setEditable(false);
                        txtTelefone.setEditable(false);
                        txtSenha.setEditable(false);
                } else {
                        atualizarTabelaEmpresa();
                }

        }

